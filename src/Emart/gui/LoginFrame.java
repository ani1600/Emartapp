    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if(validateInput()==false){
            JOptionPane.showMessageDialog(null,"Please Fill all the Fields","Incomplete Data",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String usertype=getUserType();
        if(usertype==null){
            JOptionPane.showMessageDialog(null,"Please Select User Type","Incomplete Data",JOptionPane.ERROR_MESSAGE);
            return;
        }
        UserPojo u=new UserPojo();
        u.setUserid(userid);
        u.setPassword(password);
        u.setUsertype(usertype);
        try{
            boolean result=UserDAO.validateUser(u);
            if(result==false){
              JOptionPane.showMessageDialog(null,"Invalid userid/password/usertype","Login Denied",JOptionPane.ERROR_MESSAGE);
            return;  
            }
            UserProfile.setUserid(userid);
            UserProfile.setUsertype(usertype);
            if(rbManager.isSelected()){
               ManagerFrame mf=new ManagerFrame();
               mf.setVisible(true);
               this.dispose();
            }
            else{
                ManageReceptionistFrame r=new ManageReceptionistFrame();
                r.setVisible(true);
                this.dispose();
            }
            
        }
         catch(SQLException e){
            JOptionPane.showMessageDialog(null,"DB Error","Error",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

